#include "3-calc.h"
/**
 * op_add - add
 *@a: a
 *@b: b
 *Return: always 0
**/
int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - sub
 *@b: int
 *@a: a
 *Return: always 0
 **/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - mul
 *@b: int
 *@a: a
 *Return: always 0
 **/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - div
 *@b: int
 *@a: a
 *Return: always 0
 **/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mod
 *@b: int
 *@a: a
 *Return: always 0
 **/
int op_mod(int a, int b)
{
	return (a % b);
}
